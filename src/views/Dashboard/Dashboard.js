class Dashboard extends Component {
  constructor(props) {
    super(props);

    this.toggle = this.toggle.bind(this);
    this.onRadioBtnClick = this.onRadioBtnClick.bind(this);
    this.getJobs = this.getJobs.bind(this);
    this.formatEvents = this.formatEvents.bind(this);

    this.state = {
      dropdownOpen: false,
      radioSelected: 2,
    };
  }

  componentDidMount(){
    this.getJobs()
  }

  toggle() {
    this.setState({
      dropdownOpen: !this.state.dropdownOpen,
    });
  }

  formatEvents(d){
    let events = d.map( data => {
      return {
        title: data.name || 'inspection',
        jobId: data.id,
        allDay: true,
        start: new Date(data.inspectionDate),
        end: new Date(data.inspectionDate),
      }
    })
    this.setState({calData:events})
    console.log(events)
  }

  getJobs() {
    axios.get('/api/jobs/open').then( res => {
      this.formatEvents(res.data)
    })
  }

  onRadioBtnClick(radioSelected) {
    this.setState({
      radioSelected: radioSelected,
    });
  }

  loading = () => <div className="animated fadeIn pt-1 text-center">Loading...</div>

  render() {

    return (
      <div className="animated fadeIn">

        <Row>
         <Col xs="12" sm="12" lg="2">

         </Col>
          <Col xs="12" sm="12" lg="10">
            <Card>
              <Calendar events={this.state.calData}/>
            </Card>
          </Col>
        </Row>


        <Row>
          <Col>
          <DataTable name='Inspections Today'/>
          </Col>
        </Row>
      </div>
    );
  }
}

export default Dashboard;
